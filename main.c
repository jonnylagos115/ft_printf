#include "ft_printf.h"

int		main(void)
{
	int		len[3];
	int	i = -1;

	len[0] = ft_printf("%9.2p\n", 1234);
	len[1] = ft_printf("%p\n", &i);
	len[2] = ft_printf("%.0p, %.p\n", (void*)0, (void*)0);
	while (++i < 3)
		printf("len[%d]: %d\n", i, len[i]);
	return (0);
}
